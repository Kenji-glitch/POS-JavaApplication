    public void tb_load(){
        
        try {
            
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);
            java.sql.Statement s = database.mycon().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM customer");
            
            while (rs.next()) {                
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                dt.addRow(v);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    private void Save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save_btnActionPerformed
        // insert/save button
        String name = c_name.getText();
        String tp = c_tp.getText();

        try {
            
            java.sql.Statement s = database.mycon().createStatement();
            s.executeUpdate("INSERT INTO customer (customer_name, Tp_Number) VALUES('"+name+"' , '"+tp+"')");
            JOptionPane.showMessageDialog(null, "DATA SAVED");
            
            
        } catch (SQLException e) {
            System.out.println(e);
        }
        tb_load();
    }//GEN-LAST:event_Save_btnActionPerformed

    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        // update btn function
        String name = c_name.getText();
        String tp = c_tp.getText();
        String id = c_search.getText();
        
        try {
            
            java.sql.Statement s = database.mycon().createStatement();
            s.executeUpdate("UPDATE customer SET customer_name = '"+name+"', Tp_Number = '"+tp+"' WHERE cid = '"+id+"' ");
            JOptionPane.showMessageDialog(null, "DATA UPDATED");

        } catch (Exception e) {
            System.out.println(e);
        }
        
        tb_load();
        
    }//GEN-LAST:event_update_btnActionPerformed

